public class Course {
    double MATH;
    double JAVA;
    Course(){}

    public void setJAVA(double j) {
        this.JAVA = j;
    }

    public void setMATH(double m) {
        this.MATH = m;
    }

    public double getJAVA() {
        return JAVA;
    }

    public double getMATH() {
        return MATH;
    }
}
