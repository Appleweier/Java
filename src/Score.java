public class Score {
    String num;
    String name;
    double score;
    double math;
    double java;

    public Score(){

    }
    public void setNum(String num) {
        this.num = num;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public void setJava(double java) {
        this.java = java;
    }

    public void setMath(double math) {
        this.math = math;
    }
}